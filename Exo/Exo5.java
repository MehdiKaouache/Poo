public class Exo5{
	public static void main (String[] args) {
		int demo = 0;
	
		demo = 8;
		System.out.println(demo);
		
		demo = demo + 100;
		System.out.println(demo);
		
		demo = demo - 46;
		System.out.println(demo);
		
		demo = demo * 5;
		System.out.println(demo);
		
		demo = demo / 2;
		System.out.println(demo);
		
		demo = demo % 4;
		System.out.println(demo);
	}
}